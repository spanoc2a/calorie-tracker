function interpolate(str, vars) {
  if (typeof str !== 'string') return str; // arrays/objects (ex: measurements.headers) passent tels quels
  if (!vars) return str;
  return str.replace(/\{\{(\w+)\}\}/g, (_, k) => (vars[k] !== undefined ? vars[k] : `{{${k}}}`));
}

// Traverse un chemin à segments multiples (ex: "muscuOptions.goal.prise de masse").
// Seul le DERNIER segment reçoit le suffixe _one/_other (pluriels) — les segments
// intermédiaires (namespace, sous-clé) ne sont jamais pluralisés.
function lookup(dict, segments) {
  return segments.reduce((o, k) => (o == null ? o : o[k]), dict);
}

// Hook combinant useLocale() (fr/en/es détectée + persistée) et le dictionnaire coach.
// t('ns.key', vars) ou t('ns.key.subkey', vars) — vars.count déclenche _one/_other sur le dernier segment.
export function useCoachT() {
  const { locale } = useLocale();
  const loc = COACH_T[locale] ? locale : 'fr';
  function t(path, vars) {
    const segments = path.split('.');
    const last = segments[segments.length - 1];
    const base = (vars && typeof vars.count === 'number') ? (vars.count === 1 ? last : `${last}_other`) : last;
    const baseSegments = [...segments.slice(0, -1), base];
    let val = lookup(COACH_T[loc], baseSegments);
    if (val === undefined && base !== last) val = lookup(COACH_T[loc], segments);
    if (val === undefined) val = lookup(COACH_T.fr, baseSegments);
    if (val === undefined) val = lookup(COACH_T.fr, segments);
    if (val === undefined) return path;
    return interpolate(val, vars);
  }
  return { locale: loc, t };
}
