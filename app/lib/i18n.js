'use client';
import { useState, useEffect } from 'react';

const SUPPORTED = ['fr', 'en', 'es', 'de', 'pt', 'it'];

export function detectLocale() {
  if (typeof navigator === 'undefined') return 'fr';
  const lang = (navigator.language || navigator.languages?.[0] || 'fr').split('-')[0].toLowerCase();
  return SUPPORTED.includes(lang) ? lang : 'en';
}

export function useLocale() {
  const [locale, setLocale] = useState('fr');
  useEffect(() => { setLocale(detectLocale()); }, []);
  const t = (key) => {
    const path = key.split('.');
    let val = path.reduce((o, k) => o?.[k], T[locale]);
    if (val === undefined) val = path.reduce((o, k) => o?.[k], T['en']);
    if (val === undefined) val = path.reduce((o, k) => o?.[k], T['fr']);
    return val ?? key;
  };
  return { locale, t };
}

